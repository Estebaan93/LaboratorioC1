    private void jButton24ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton24ActionPerformed
          this.segundoNumero=Float.parseFloat(this.casilla.getText());       //Capturamos de la casilla el segundo numero, lo convertimos de String a float
          switch (this.operador) {
            case "+": this.casilla.setText(sinCero(this.primerNumero+this.segundoNumero));            //Mostramos la suma del 1er nro + 2do nro, caso swich +  
                break;
                case "-": this.casilla.setText(sinCero(this.primerNumero-this.segundoNumero));         //Mostramos la resta del 1er nro - 2do nro, caso swich -
                break;
                case "*": this.casilla.setText(sinCero(this.primerNumero*this.segundoNumero));       //Mostramos la mult del 1er nro * 2do nro, caso swich *
                break;
                case "/":
                    if (this.segundoNumero==0) {
                         this.casilla.setText("No se puede dividir por 0");
                    } else {
                         this.casilla.setText(sinCero(this.primerNumero/this.segundoNumero));       //Mostramos la div del 1er nro / 2do nro, caso swich /
                    }
                break;
            default:
                throw new AssertionError();
        }
          
    }//GEN-LAST:event_jButton24ActionPerformed

