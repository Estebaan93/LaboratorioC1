       //Metodo para quita el punto cuando el resultado es un nro entero
        //Ahora podemos usar esta funcion en nuestro operador
      public String sinCero(float resultado){
          String retorno="";
          retorno=Float.toString(resultado);                //Transformamos a string 
          if (resultado%1==0) {                                     //Por residuo igual a punto 0. Es un numero entero
              retorno=retorno.substring(0, retorno.length()-2);             //Recortamos si el valor da un nro entero, desde posicion 0 a largo - 2
          }
          
          return retorno;
      }
    
